    private void clearLabel(){
        this.selectedSeats.clear(); // clear the array list
        this.lblTicket.setText(null);
        this.lblTot.setText(null);
        this.lblSeat.setText(null);
        this.lblSeat.setText(null);
        this.tot = 0;
        this.tickets = 0;
    }
    
    
